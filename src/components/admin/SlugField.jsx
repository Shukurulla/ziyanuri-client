const translitMap = {
  "а": "a", "б": "b", "в": "v", "г": "g", "д": "d", "е": "e", "ё": "yo", "ж": "j",
  "з": "z", "и": "i", "й": "y", "к": "k", "л": "l", "м": "m", "н": "n", "о": "o",
  "п": "p", "р": "r", "с": "s", "т": "t", "у": "u", "ф": "f", "х": "x", "ц": "ts",
  "ч": "ch", "ш": "sh", "щ": "shch", "ъ": "", "ы": "i", "ь": "", "э": "e", "ю": "yu",
  "я": "ya", "ў": "w", "қ": "q", "ғ": "g", "ҳ": "h", "ң": "n", "ә": "a", "ө": "o",
  "ü": "u", "ö": "o", "ı": "i", "ş": "sh", "ç": "ch", "ğ": "g", "ń": "n", "á": "a",
  "é": "e", "ó": "o", "ú": "u", "ǵ": "g", "ś": "s",
  "'": "", "'": "", "`": "", "«": "", "»": "",
};

export function toSlug(text) {
  if (!text) return "";
  return text
    .toLowerCase()
    .split("")
    .map((c) => translitMap[c] || c)
    .join("")
    .replace(/[^a-z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
}
